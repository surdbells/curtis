    public void sendSignature() {
        TelephonyManager telephonyManager;
        telephonyManager = (TelephonyManager) getSystemService(Context.
                TELEPHONY_SERVICE);
        if (ActivityCompat.checkSelfPermission(SignatureActivity.this, Manifest.permission.READ_PHONE_STATE) != PackageManager.PERMISSION_GRANTED) {
            // TODO: Consider calling
            //    ActivityCompat#requestPermissions
            // here to request the missing permissions, and then overriding
            //   public void onRequestPermissionsResult(int requestCode, String[] permissions,
            //                                          int[] grantResults)
            // to handle the case where the user grants the permission. See the documentation
            // for ActivityCompat#requestPermissions for more details.
            return;
        }
        final String deviceId = telephonyManager.getDeviceId();
        Bitmap signatureBitmap = mSignaturePad.getSignatureBitmap();
        final String signatureBase64 = encodeToBase64(signatureBitmap, Bitmap.CompressFormat.JPEG, 1);
        // Toast.makeText(SignatureActivity.this, signatureBase64, Toast.LENGTH_SHORT).show();
        Intent intent = getIntent();
        final String curJobid = intent.getStringExtra("Job_id");
        DatabaseReference myRef = database.getReference("baseUrl");
                myRef.addValueEventListener(new ValueEventListener() {
            @Override
            public void onDataChange(DataSnapshot dataSnapshot) {
                progressDialog.setMessage("Processing your request...");
                progressDialog.show();
                String baseUrlValue = (String) dataSnapshot.getValue();
                FirebaseUser user = firebaseAuth.getCurrentUser();
                Date now = new Date();
                SimpleDateFormat currentDate = new SimpleDateFormat("dd/MM/yyyy hh:mm aaa");
                final String dateNow =  currentDate.format(now);
                final String userID = user.getUid();
                final String API_URL = baseUrlValue + "postSignature";

                StringRequest stringRequest = new StringRequest(Request.Method.POST, API_URL,
                        new Response.Listener<String>() {
                            @Override
                            public void onResponse(String response) {
                                Toast.makeText(SignatureActivity.this, response, Toast.LENGTH_LONG).show();
                                Intent intent = new Intent(SignatureActivity.this, DashboardActivity.class);
                                startActivity(intent);
                            }
                        },
                        new Response.ErrorListener() {
                            @Override
                            public void onErrorResponse(VolleyError error) {
                                Toast.makeText(SignatureActivity.this, "Not completed, Pls Try Again", Toast.LENGTH_LONG).show();
                            }
                        }){
                    @Override
                    protected Map<String,String> getParams(){
                        Map<String,String> params = new HashMap<String, String>();
                        params.put(REF_NUMBER, curJobid);
                        params.put(SIGNATURE, signatureBase64);
                        params.put(DATE_NOW, dateNow);
                        params.put(LONGITUDE, CurLongitude);
                        params.put(LATITUDE, CurLatitude);
                        params.put(USER_ID, userID);
                        params.put(DEVICE_ID, deviceId);
                        return params;
                    }

                };

                RequestQueue requestQueue = Volley.newRequestQueue(SignatureActivity.this);
                stringRequest.setRetryPolicy(new DefaultRetryPolicy(
                        5000,
                        DefaultRetryPolicy.DEFAULT_MAX_RETRIES,
                        DefaultRetryPolicy.DEFAULT_BACKOFF_MULT));
                requestQueue.add(stringRequest);
            }
            @Override
            public void onCancelled(DatabaseError databaseError) {
                Toast.makeText(SignatureActivity.this, "Oops " + databaseError.getMessage(), Toast.LENGTH_LONG).show();
            }
        });
    }
    public void getLastLocation() {
        if (ActivityCompat.checkSelfPermission(this, Manifest.permission.ACCESS_FINE_LOCATION) != PackageManager.PERMISSION_GRANTED && ActivityCompat.checkSelfPermission(this, Manifest.permission.ACCESS_COARSE_LOCATION) != PackageManager.PERMISSION_GRANTED) {
            // TODO: Consider calling
            //    ActivityCompat#requestPermissions
            // here to request the missing permissions, and then overriding
            //   public void onRequestPermissionsResult(int requestCode, String[] permissions,
            //                                          int[] grantResults)
            // to handle the case where the user grants the permission. See the documentation
            // for ActivityCompat#requestPermissions for more details.
            return;
        }
        CurtisLocationClient.getLastLocation()
                .addOnCompleteListener(this, new OnCompleteListener<Location>() {
                    @Override
                    public void onComplete(@NonNull Task<Location> task) {
                        if (task.isSuccessful() && task.getResult() != null) {
                            mLastLocation = task.getResult();
                            CurLongitude = String.valueOf(mLastLocation.getLongitude());
                            CurLatitude = String.valueOf(mLastLocation.getLatitude());
                        } else {
                            Toast.makeText(SignatureActivity.this, "Location not Found, Try Again", Toast.LENGTH_SHORT).show();
                        }
                    }
                });

    }

    // enconding bitmap in base64
    public static String encodeToBase64(Bitmap image, Bitmap.CompressFormat compressFormat, int quality)
    {
        ByteArrayOutputStream byteArrayOS = new ByteArrayOutputStream();
        image.compress(compressFormat, quality, byteArrayOS);
        return Base64.encodeToString(byteArrayOS.toByteArray(), Base64.DEFAULT);
    }

